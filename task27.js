// Version where the alien is green
var alienColorGreenn = 'green';
if (alienColorGreenn === 'green') {
    console.log("Congratulations! You just earned 5 points for shooting the green alien.");
}
else if (alienColorGreenn === 'yellow') {
    console.log("Congratulations! You just earned 10 points for shooting the yellow alien.");
}
else if (alienColorGreenn === 'red') {
    console.log("Congratulations! You just earned 15 points for shooting the red alien.");
}
else {
    console.log("Unknown alien color. No points earned.");
}
