let animals: string[] = ["Cat", "lion", "Bear"];


for (const animal of animals) {
  if (animal === "Dog") {
    console.log("Dogs are loyal companions.");
  } else if (animal === "Cat") {
    console.log("Cats are independent and playful.");
  } else if (animal === "Rabbit") {
    console.log("Rabbits are cute and make great indoor pets.");
  }
}

console.log("\nAll of these animals have unique qualities that make them wonderful pets!");