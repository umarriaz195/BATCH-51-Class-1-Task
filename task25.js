// Passing version
var alienColorPass = 'green';
if (alienColorPass === 'green') {
    console.log("Congratulations! You just earned 5 points!");
}
else {
    console.log("No points earned.");
}
// Failing version
var alienColorFail = 'red';
if (alienColorFail === 'green') {
    console.log("Congratulations! You just earned 5 points!");
}
else {
    console.log("No points earned.");
}
